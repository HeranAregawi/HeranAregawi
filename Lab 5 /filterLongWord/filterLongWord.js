const filterLongWord = function(w, leng){
    const newArray = [];
    for(let i = 0 ; i < w.length ; i++){
        if(w[i].length > leng){
            newArray.push;
        }
    }
    return newArray;
}

elements = new Array('hydrogen', 'heran', 'momona');

console.log(filterLongWord(elements, 4));

